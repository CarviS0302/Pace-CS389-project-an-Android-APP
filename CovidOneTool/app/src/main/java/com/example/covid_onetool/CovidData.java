package com.example.covid_onetool;

public class CovidData {
    private String xArea;
    private String heal;
    private String died;
    private String confirm;
    private String curConfirm;
    private String city;
    private double latitude;
    private double longitude;
    private String code;

    public String getxArea() {
        return xArea;
    }

    public void setxArea(String xArea) {
        this.xArea = xArea;
    }

    public String getHeal() {
        return heal;
    }

    public void setHeal(String heal) {
        this.heal = heal;
    }

    public String getDied() {
        return died;
    }

    public void setDied(String died) {
        this.died = died;
    }

    public String getConfirm() {
        return confirm;
    }

    public void setConfirm(String confirm) {
        this.confirm = confirm;
    }

    public String getCurConfirm() {
        return curConfirm;
    }

    public void setCurConfirm(String curConfirm) {
        this.curConfirm = curConfirm;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
